public class Penumpang {
    String nama;
    String kotaAsal, kotaTujuan;
    int harga, jumlahTiket;

public Penumpang(String nama, String kotaAsal, String kotaTujuan, int jml, int harga ){
    this.nama = nama;
    this.kotaAsal = kotaAsal;
    this.kotaTujuan = kotaTujuan;
    jumlahTiket = jml;
    this.harga = harga;
}

}